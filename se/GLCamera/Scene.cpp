void PaintScene(Size sz)
{
    static GLCode ourShader(R"(
		#version 330 core
		in vec3 aPos;
		in vec2 aTexCoord;
		
		out vec2 TexCoord;
		
		uniform mat4 model;
		uniform mat4 view;
		uniform mat4 projection;
		
		void main()
		{
			gl_Position = projection * view * model * vec4(aPos, 1.0f);
			TexCoord = vec2(aTexCoord.x, aTexCoord.y);
		}
	)", R"(
		#version 330 core
		out vec4 FragColor;
		
		in vec2 TexCoord;
		
		// texture samplers
		uniform sampler2D texture1;
		uniform sampler2D texture2;
		
		void main()
		{
			// linearly interpolate between both textures (80% container, 20% awesomeface)
			FragColor = mix(texture(texture1, TexCoord), texture(texture2, TexCoord), 0.2);
		}
	)");
	
	DLOG("PAINT");

    // per-frame time logic
    // --------------------
    float currentFrame = msecs() / 1000.0f;
    deltaTime = currentFrame - lastFrame;
    lastFrame = currentFrame;
    
    // input
    // -----
//    processInput(window);

        // render
        // ------
    glClearColor(0.2f, 0.3f, 0.3f, 1.0f);
	glClear(GL_COLOR_BUFFER_BIT | GL_DEPTH_BUFFER_BIT); 
	
	texture1.Bind(0);
	texture2.Bind(1);

	// activate shader
    ourShader.Use();

    // pass projection matrix to shader (note that in this case it could change every frame)
    glm::mat4 projection = glm::perspective(glm::radians(fov), sz.cx / (float)sz.cy, 0.1f, 100.0f);
    ourShader.Mat4("projection", &projection[0][0]);

    // camera/view transformation
    glm::mat4 view = glm::lookAt(cameraPos, cameraPos + cameraFront, cameraUp);
    ourShader.Mat4("view", &view[0][0]);

        // render boxes
    glBindVertexArray(VAO);
    for (unsigned int i = 0; i < 10; i++)
    {
        // calculate the model matrix for each object and pass it to shader before drawing
        glm::mat4 model = glm::mat4(1.0f); // make sure to initialize matrix to identity matrix first
        model = glm::translate<float>(model, cubePositions[i]);
        float angle = 20.0f * i;
        model = glm::rotate<float>(model, glm::radians(angle), glm::vec3(1.0f, 0.3f, 0.5f));
        ourShader.Mat4("model", &model[0][0]);

        glDrawArrays(GL_TRIANGLES, 0, 36);
    }
}

